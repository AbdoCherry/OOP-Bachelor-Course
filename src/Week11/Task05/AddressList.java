package Week11.Task05;

public class AddressList {

    private String name;
    private int year;

    public AddressList() {

    }

    public AddressList(String name, int year) {
        this.name = name;
        this.year = year;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }
}
